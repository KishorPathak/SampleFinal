    public String serverRegister(JSONObject userJSON)
            throws UnsupportedEncodingException {
        final String baseUrl = "http://10.222.120.101:8080/stayfit/registerApp";
        String outResponse = "blank";

        String text = "";


        try {
            HttpClient httpclient = new DefaultHttpClient();
            HttpPost httppost = new HttpPost(baseUrl);
            StringEntity se = new StringEntity(userJSON.toString());

//sets the post request as the resulting string
            httppost.setEntity(se);
//sets a request header so the page receving the request
//will know what to do with it
            httppost.setHeader("Accept", "application/json");
            httppost.setHeader("Content-type", "application/json");

// Execute HTTP Post Request
            HttpResponse response = httpclient.execute(httppost);
            InputStream responseStream = response.getEntity().getContent();
            BufferedReader reader = new BufferedReader(new InputStreamReader(responseStream));
            StringBuilder out = new StringBuilder();
            String line;
            while ((line = reader.readLine()) != null) {
                out.append(line);
            }
            text = out.toString();
        } catch (ClientProtocolException e) {
            e.printStackTrace();
        } catch (UnsupportedEncodingException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }
        // Show response on activity
        // tvDisp.setText(text);
        return text;
    }


    /**
     * Attempts to sign in or register the account specified by the login form.
     * If there are form errors (invalid email, missing fields, etc.), the
     * errors are presented and no actual login attempt is made.
     */
    private void attemptLogin() {
        JSONObject userJson=new JSONObject();
        if (mAuthTask != null) {
            return;
        }

        // Reset errors.
        mEmailView.setError(null);
        mPasswordView.setError(null);

        // Store values at the time of the login attempt.
        String email = mEmailView.getText().toString();
        String password = mPasswordView.getText().toString();
        String userName = mUserName.getText().toString();
        String dob = mDOB.getText().toString();
        String bu = mAtvBU.getText().toString();
        String password_cnf = mpassConf.getText().toString();


        boolean cancel = false;
        View focusView = null;

        // Check for a valid password, if the user entered one.
        if (!TextUtils.isEmpty(password) && !isPasswordValid(password)) {
            mPasswordView.setError(getString(R.string.error_invalid_password));
            focusView = mPasswordView;
            cancel = true;
        }

        if (!TextUtils.isEmpty(password_cnf) && !isPasswordValid(password)) {
            mpassConf.setError(getString(R.string.error_invalid_password));
            focusView = mpassConf;
            cancel = true;
        }
        if(!password.equals(password_cnf))        {
            mpassConf.setError("Password Not Matched");
            focusView = mpassConf;
            cancel = true;
        }
        // Check for a valid email address.
        if (TextUtils.isEmpty(email)) {
            mEmailView.setError(getString(R.string.error_field_required));
            focusView = mEmailView;
            cancel = true;
        } else if (TextUtils.isEmpty(userName)) {
            mUserName.setError(getString(R.string.error_field_required));
            focusView = mUserName;
            cancel = true;
        }
        else if (TextUtils.isEmpty(bu)) {
            mAtvBU.setError(getString(R.string.error_field_required));
            focusView = mAtvBU;
            cancel = true;
        } else if (TextUtils.isEmpty(dob)) {
            mDOB.setError(getString(R.string.error_field_required));
            focusView = mDOB;
            cancel = true;
        }else if (!isEmailValid(email)) {
            mEmailView.setError(getString(R.string.error_invalid_email));
            focusView = mEmailView;
            cancel = true;
        }
        if (cancel) {
            // There was an error; don't attempt login and focus the first
            // form field with an error.
            focusView.requestFocus();
        } else {
            // Show a progress spinner, and kick off a background task to
            // perform the user login attempt.
            showProgress(true);

            try {
                userJson.put("full_name", userName);
                userJson.put("email", email);
                userJson.put("dob",dob);
                userJson.put("bu", bu);
                userJson.put("password", password);
                userJson.put("passwordConfirm", password_cnf);
            } catch (JSONException e) {
                e.printStackTrace();
            }

            mAuthTask = new UserLoginTask(userJson);
            Log.d(TAG, "attemptLogin: User JSON" +userJson.toString());
            mAuthTask.execute((Void) null);
        }
    }

